/*
---------------------------------------------------------------------------------------
	Definicja i deklaracija funkcije
---------------------------------------------------------------------------------------
	type-specifier return-type function-name(parameters){
		declarations;
		statements;
		return value;
	}
---------------------------------------------------------------------------------------
	0. Prototip funkcije
---------------------------------------------------------------------------------------
	Funkcija mora biti deklarirana prije korištenja
	int add(int a, int b); -------> Prototip
---------------------------------------------------------------------------------------
	Bez prototipa, ovo bi bilo UB
---------------------------------------------------------------------------------------
	1. type-specifier
---------------------------------------------------------------------------------------
	Određuje vidljivost, koristi se static ili ništa
---------------------------------------------------------------------------------------
---------------------------------------------------------------------------------------
	2. return type
---------------------------------------------------------------------------------------
	2.1 Ako je funkcija tipa void, ne smije vratiti ništa
---------------------------------------------------------------------------------------
	return; -----> ok
	return 5; ---> Error
---------------------------------------------------------------------------------------
	2.2 Više return naredbi
---------------------------------------------------------------------------------------
	Dozvoljeno i često korisno
---------------------------------------------------------------------------------------
	int divide(int a, int b){
		if(b == 0)
			return -1;
		return a/b;
	}
---------------------------------------------------------------------------------------
	2.3 Povrat pokazivača
---------------------------------------------------------------------------------------
	int* func(){
		int x = 5;
		return &x; ----> Error, povrat lokalne varijable
	}
---------------------------------------------------------------------------------------
	Ispravno: 
	1) Povrat globalne varijable
	2) Static specifikator
	3) Dinamička memorija
---------------------------------------------------------------------------------------
	3. function-name
---------------------------------------------------------------------------------------
	Jedinstveno ime funkcije, funkcija i varijabla, istog dosega se ne mogu zvati
	isto
---------------------------------------------------------------------------------------
	Mora biti validan C identifikator, Case sensitive; sum != Sum
---------------------------------------------------------------------------------------
	Preporuka: glagol + Imenica = calculateSum , readFile, initBuffer;
---------------------------------------------------------------------------------------
	4. parameters
---------------------------------------------------------------------------------------
	Lista deklaracija varijabli, koje predstavljaju parametre funkcije, međusobno
	odvojene zarezom, u deklaracija ne moraju se navesti imena parametera, u definiciji
	funkcije su imena parametara obavezni
---------------------------------------------------------------------------------------
	4.1 int add(int a, int b)
---------------------------------------------------------------------------------------
	a) ako funkcija kao ova gore, prima više parametara, parametri su lokalne kopije
	prosljeđuju se pass by value
---------------------------------------------------------------------------------------
	b) Jedini način da funkcija promijeni vanjske podatke je preko pokazivača
---------------------------------------------------------------------------------------
	void increment(int* x){
		(*x)++;
	}
---------------------------------------------------------------------------------------
	4.2 ako funkcija ne prima parametre, obavezno staviti void u potpis funkcije
---------------------------------------------------------------------------------------
	void func(void); -----> Ispravno
	void func(); ---------> Neispravno
---------------------------------------------------------------------------------------
	4.3 Polja kao parametri
---------------------------------------------------------------------------------------
	void printArray(int arr[], int size)
---------------------------------------------------------------------------------------
	arr[] je zapravo int* arr
---------------------------------------------------------------------------------------
	Ime polja kada se proslijeđuje funkcija, "decay-a" u pokazivač, gdje je ime polja
	adresa prvog elementa
---------------------------------------------------------------------------------------
	Kada se prolijeđuje ime polja funkciji, kao u primjeru gore, obavezno proslijediti
	veličinu polja funkciji, jer ono nema mogućnosti, da sazna veličinu, polja dinamički
---------------------------------------------------------------------------------------
	4.4 Const parameteri
---------------------------------------------------------------------------------------
	void print(const int** arr, int size)
---------------------------------------------------------------------------------------
	Zaštita podatka
---------------------------------------------------------------------------------------
	5. Rekurzija
---------------------------------------------------------------------------------------
	Funkcija može zvati samu sebe, i to se zove rekurzija, mora postojati base case
---------------------------------------------------------------------------------------
	int fact(int n){
		if(n <= 1)
			return 1;
		return n*fact(n-1)
	}
---------------------------------------------------------------------------------------
	6. Inline funkcije
---------------------------------------------------------------------------------------
	inline int square(int x)
		return x*x;
---------------------------------------------------------------------------------------
	Više optimizacija, prijedlog kompajleru, nije naredba
---------------------------------------------------------------------------------------
	7. Pokazivač na funkciju
---------------------------------------------------------------------------------------
	Funkcije su adrese u memoriji
---------------------------------------------------------------------------------------
	int (*fp)(int, int)
		fp = add;
	//Poziv te funkcije
	fp(2, 3);
	//5
---------------------------------------------------------------------------------------
	Pokazivači na funkcije se često nalaze u 
	1) Callbackovima
	2) State machine-ovima
	3) Embeded sustavima
---------------------------------------------------------------------------------------
*/
